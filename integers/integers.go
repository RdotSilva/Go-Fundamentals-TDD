package integers

// Take two integers and return the sum
func Add(x, y int) int {
	return x + y
}

// Take two integers and return the difference
func Subtract(x, y int) int {
	return x - y
}

// Take two integers and return the product
func Multiply(x, y int) int {
	return x * y
}

// Take two integers and return the quotient
func Divide(x, y int) int {
	return x / y
}